<script>
    // 1. Mobile Menu Drawer Toggle
    const menuToggleBtn = document.getElementById('mobile-menu-toggle');
    const mobileDrawer = document.getElementById('mobile-drawer');
    const hamburgerIcon = document.getElementById('hamburger-icon');
    const closeIcon = document.getElementById('close-icon');

    menuToggleBtn.addEventListener('click', () => {
        const isOpen = mobileDrawer.classList.contains('scale-y-100');
        if (isOpen) {
            mobileDrawer.classList.remove('scale-y-100', 'opacity-100');
            mobileDrawer.classList.add('scale-y-95', 'opacity-0');
            setTimeout(() => {
                mobileDrawer.classList.add('hidden');
            }, 200);
            hamburgerIcon.classList.remove('hidden');
            closeIcon.classList.add('hidden');
        } else {
            mobileDrawer.classList.remove('hidden');
            setTimeout(() => {
                mobileDrawer.classList.add('scale-y-100', 'opacity-100');
                mobileDrawer.classList.remove('scale-y-95', 'opacity-0');
            }, 10);
            hamburgerIcon.classList.add('hidden');
            closeIcon.classList.remove('hidden');
        }
    });


</script>